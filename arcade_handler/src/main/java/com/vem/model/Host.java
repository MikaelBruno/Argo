package com.vem.model;

public class Host {
    private String ip;
    private String nic;

    // Getters and setters
    public String getip() {
        return ip;
    }

    public void setip(String ip) {
        this.ip = ip;
    }

    public String getnic() {
        return nic;
    }

    public void setnic(String nic) {
        this.nic = nic;
    }
}
